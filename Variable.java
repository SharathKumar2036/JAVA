package New;
public class Variable {

	public static void main(String[] args) {
		byte k=127;
		System.out.println("byte " +k);//Size 1 byte
		int i=20;
		System.out.println("Integer"+i);//Size 4bytes
		double b=40d;//Size 8bytes
		System.out.println("Double" +b);
		char a='d';
		System.out.println("Character" +a);//Size 2bytes
		float c=12.54f;
		System.out.println("float" +c);//Size 4bytes
		long d=1208L;
		System.out.println("Long " +d);//Size 8bytes
		boolean e= true;
		System.out.println("Boolean " +e);// Size 1bit
		short f=10_298;
		System.out.println("short " +f);//Size 2bytes
		
		
		
		
		
		

	}

}
